print("Python has 3 numeric types: int, float, complex"); 

myValue = 1

print(myValue)

print(type(myValue))

print(str(myValue) + " is of data type " + str(type(myValue)))

floatValue = 3.14

print(floatValue)

print(type(floatValue))

print(str(floatValue) + " is of data type " + str(type(floatValue)))

complexValue = 5j
print(complexValue)
print(type(complexValue))
print(str(complexValue) + " is of data type " + str(type(complexValue)))

bool = True
print(bool)
print(type(bool))
print(str(bool) + " is data type " + str(type(bool)))